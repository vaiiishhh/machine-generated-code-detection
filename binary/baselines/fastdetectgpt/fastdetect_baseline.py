def get_fast_detect_score(text):
    if not isinstance(text, str) or len(text.strip()) == 0:
        return None

    tokenized = tokenizer(
        text,
        return_tensors="pt",
        padding=True,
        truncation=True,
        max_length=512,
        return_token_type_ids=False
    ).to(device)
    if tokenized.input_ids.size(1) < 2:
        return None

    labels = tokenized.input_ids[:, 1:]

    with torch.no_grad():
        logits = model(**tokenized).logits[:, :-1]
        # Fast-DetectGPT uses a reference model (to sample) and a scoring model (to score).
        logits_ref = logits
        logits_score = logits
        try:
            discrepancy = get_sampling_discrepancy_analytic(logits_ref, logits_score, labels)
            return float(discrepancy)
        except Exception as e:
            return None

