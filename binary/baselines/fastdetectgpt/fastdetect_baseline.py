def print_breakdown_table(dataframe, column_name, table_title):
    print("\n" + "="*85)
    print(f"📊 {table_title}")
    print("="*85)
    print(f"{column_name.capitalize():<15} | {'AUROC':<8} | {'Precision':<9} | {'Recall':<8} | {'F1 Score':<8} | {'Accuracy':<8}")
    print("-" * 85)

    unique_vals = dataframe[column_name].dropna().unique()
    for val in sorted(unique_vals):
        subset = dataframe[dataframe[column_name] == val]

        if len(subset) > 0 and len(subset['label'].unique()) > 1:
            auroc = roc_auc_score(subset['label'], subset['fast_detect_score'])
            acc = accuracy_score(subset['label'], subset['prediction'])
            prec = precision_score(subset['label'], subset['prediction'], zero_division=0)
            rec = recall_score(subset['label'], subset['prediction'], zero_division=0)
            f1 = f1_score(subset['label'], subset['prediction'], zero_division=0)

            print(f"{str(val):<15} | {auroc:<8.4f} | {prec:<9.4f} | {rec:<8.4f} | {f1:<8.4f} | {acc:<8.4f}")
    print("="*85)


